     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $user = Auth::user();
        $code = Str::random(6);
        $id = $user->id;

        Code::create([
            'code' => $code,
            'id_user' => $id
        ]);

        return redirect('/codes')->with('newCode', $code);
    }

